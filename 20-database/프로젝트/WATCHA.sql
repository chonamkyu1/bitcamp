CREATE TABLE MOVIE(
    MOVIE_ID NUMBER PRIMARY KEY,
    TITLE VARCHAR2(50) NOT NULL,
    STORY VARCHAR2(50) NOT NULL,
    GRADE VARCHAR2(20) NOT NULL,
    OPENYEAR VARCHAR2(20) NOT NULL,
    CAMEO VARCHAR2(45) NOT NULL,
    DIRECTOR VARCHAR2(45) NOT NULL,
    CATEGORY VARCHAR2(45) NOT NULL,
    GENRE VARCHAR2(45) NOT NULL  
);
